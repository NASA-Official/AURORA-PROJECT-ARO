package com.nassafy.batch.dto.response;

public class Coordinates {
    private Float lat;
    private Float lon;

    // Getters and Setters
    public Float getLat() {
        return lat;
    }

    public void setLat(Float lat) {
        this.lat = lat;
    }

    public Float getLon() {
        return lon;
    }

    public void setLon(Float lon) {
        this.lon = lon;
    }
}
